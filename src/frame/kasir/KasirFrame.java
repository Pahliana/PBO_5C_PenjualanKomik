    private void tampil_namakomik() {
        try {
            comboNama.addItem("Pilih Komik");
            pst = con.prepareStatement("select nama_komik from komik");
            rs = pst.executeQuery();
            while(rs.next()){
                comboNama.addItem(rs.getString(1));
               
            }
            
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }
    
    
