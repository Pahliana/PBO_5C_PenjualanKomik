    private void comboNamaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_comboNamaItemStateChanged
        try {
            pst = con.prepareStatement("select * from komik where nama_komik=?");
            pst.setString(1, comboNama.getSelectedItem().toString());
            rs = pst.executeQuery();
            if(rs.next()){
                HargaKomik.setText(rs.getString(5)); //mengambil data dari tabel
                
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_comboNamaItemStateChanged

